    public void sales(){
        int lastid = 0;
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/pizza?allowPublicKeyRetrieval=true&useSSL=false","root","Korir#001");
            
            String total = txtTotal.getText();
            String bal = txtBalance.getText();
            String pay = txtPay.getText();
            
            String query = "insert into sales1(subtotal, pay, bal) values(?,?,?)";
            pst = con.prepareStatement(query);
            pst.setString(1, total);
            pst.setString(2, pay);
            pst.setString(3, bal);
            pst.executeQuery();
            
            ResultSet rs = pst.getResultSet();
            
            if(rs.next()){
                lastid = rs.getInt(1);
            }
            
            int row = table.getRowCount();
            
            String query1 = "insert into sales_product1(sales_id, prodname, price, qty, total) values(?,?,?,?,?)";
            pst1 = con.prepareStatement(query1);
            
            String podname = "";
            int price;
            int qty;
            int tot = 0;
            
            for(int i = 0; i<table.getRowCount(); i++){
                podname = (String) table.getValueAt(i, 0);
                price = (int) table.getValueAt(i, 1);
                qty = (int) table.getValueAt(i, 2);
                tot = (int) table.getValueAt(i, 3);
                
                pst1.setInt(1, lastid);
                pst1.setString(2, podname);
                pst1.setInt(3, price);
                pst1.setInt(4, qty);
                pst1.setInt(5, tot);
                pst1.executeQuery();
                
            }
            
            JOptionPane.showMessageDialog(this, "Sales Completed successfully");
            
            
        
        }
        catch(Exception e){
            e.printStackTrace();
        }
         
    }

    
    
