    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if (rd1.isSelected() == true) {
            item = "Small";
            price = 200;
        }

        if (rd2.isSelected() == true) {
            item = "Medium";
            price = 200;
        }

        if (rd3.isSelected() == true) {
            item = "Large";
            price = 200;
        }

        if (rd4.isSelected() == true) {
            item = "Extra Large";
            price = 200;
        }

        int qty = Integer.parseInt(txtQty.getValue().toString());
        int total = qty * price;

        model = (DefaultTableModel) table.getModel();
        model.addRow(new Object[]{
            item,
            price,
            qty,
            total

        });
        int sum = 0;
        for (int a = 0; a < table.getRowCount(); a++) {
            sum = sum + Integer.parseInt(table.getValueAt(a, 3).toString());

        }
        txtTotal.setText(Integer.toString(sum));

    }//GEN-LAST:event_btnAddActionPerformed

